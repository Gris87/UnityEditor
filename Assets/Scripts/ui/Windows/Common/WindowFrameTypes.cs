namespace UI.Windows.Common
{
	public enum WindowFrameTypes
	{
		/// <summary>
		/// Window without frame.
		/// </summary>
		Frameless
		,
		/// <summary>
		/// Normal window frame.
		/// </summary>
		Window
		,
		/// <summary>
		/// Window frame that used for subwindows.
		/// </summary>
		SubWindow
		,
		/// <summary>
		/// Indicates that the window is a Macintosh drawer.
		/// </summary>
		Drawer
	}
}

