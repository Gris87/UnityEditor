namespace Common.UI.DockWidgets
{
	/// <summary>
	/// This enum type is used to specify docking area orientation.
	/// </summary>
	public enum DockingAreaOrientation
	{
		/// <summary>
		/// If there is no dock widgets (or just one dock widget) in docking area then there is no orientation.
		/// </summary>
		None
		,
		/// <summary>
		/// Horizontal orientation.
		/// </summary>
		Horizontal
		,
		/// <summary>
		/// Vertical orientation.
		/// </summary>
		Vertical
	}
}

